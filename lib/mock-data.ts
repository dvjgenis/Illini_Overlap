export interface Course {
  code: string
  title: string
  genEds?: GenEdCategory[] // Which Gen Ed requirements this course fulfills
}

export type ConfidenceLevel = "high" | "medium" | "low"

// UIUC Gen Ed Categories based on official requirements
// See: https://las.illinois.edu/students/requirements/gened
export type GenEdCategory =
  | "COMP1" // Composition I (RHET 105, CMN 111+112, etc.)
  | "ACP" // Advanced Composition
  | "HUM-LA" // Humanities: Literature & the Arts
  | "HUM-HP" // Humanities: Historical & Philosophical Perspectives
  | "NAT-LS" // Natural Sciences: Life Sciences
  | "NAT-PS" // Natural Sciences: Physical Sciences
  | "SBS-SS" // Social Sciences
  | "SBS-BSC" // Behavioral Sciences
  | "CS-NW" // Cultural Studies: Non-Western
  | "CS-US" // Cultural Studies: US Minority Cultures
  | "CS-WCC" // Cultural Studies: Western/Comparative Cultures
  | "QR1" // Quantitative Reasoning I (required)
  | "QR2" // Quantitative Reasoning II

// Parent categories for grouping
export type GenEdParentCategory = "COMP" | "HUM" | "NAT" | "SBS" | "CS" | "QR"

export interface GenEdRequirement {
  id: GenEdCategory
  parentCategory: GenEdParentCategory
  name: string
  shortName: string
  hoursRequired: number
  description: string
  note?: string
}

